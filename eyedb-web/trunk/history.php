<h1>History</h1>

<p>A first prototype, IDB, was developed at <a href="http://www.genethon.fr">Genethon laboratories</a> for the Genome View project.</p>

<p>The Genome View project was initiated in 1992 to store and facilitate access to the human genome data produced at Genethon (physical and genetic maps).</p>

<p>The Genome View environment includes an integrated database of the human genome, HUGEMAP, a graphical interface, WWW and mail servers, and a C API. </p>

<p>Since April 1994, a new version has being developed at SYSRA with partial fundings from the ANVAR (Agence Nationale pour la Valorisation de la Recherche), the Conseil Regional d'Ile de France and the European Contract EC BIO4-CT96-0346.</p>

<p>This second version of EyeDB, is a complete rewrite. </p>

<p>Since 1994, EyeDB has been used in a lot of bioinformatics projects
in collaboration with the <a href="http://www.infobiogen.fr">
CRI Infobiogen</a> and european projects
with the <a href="http://www.ebi.ac.uk">EBI</a>.
</p>

