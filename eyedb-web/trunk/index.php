<h1>Welcome to EyeDB</h1>

<p>
EyeDB is an Object Oriented Database Management System (OODBMS) based
on the ODMG 3.0 specification.
</p>

<p>
EyeDB features an advanced object model (inheritance, collections,
arrays, methods, triggers, constraints, reflexivity), a type
definition language based on ODMG ODL, an object query and
manipulation language based on ODMG OQL and programming interfaces for
C++ and Java.
</p>

